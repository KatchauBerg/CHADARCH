TOTAL = len(ART)
def line_color(i):
    if i <= 6:
        t = i / 6
        r = int(180 + 22 * t)
        g = int(40  - 10 * t)
        b = int(20  - 10 * t)
    elif i <= 8:
        r, g, b = 231, 76, 60
    else:
        t = (i - 9) / (TOTAL - 9)
        r = int(200 - 60 * t)
        g = int(50  - 40 * t)
        b = int(35  - 25 * t)
    return fg(r, g, b)

output = []
for i, line in enumerate(ART):
    color = line_color(i)
    row = []
    for ch in line:
        if ch == BLANK or ch == " ":
            row.append(ch)
        else:
            row.append(f"{color}{ch}{RST}")
    output.append("".join(row))

sys.stdout.write("\n".join(output) + "\n")
